public class Negativesum {
    public static void main(String args[]){
        int a=2, b=-3, c=-14, d=7;
        int sum=0;
        if(a<0)
            sum+=a;
        if(b<0)
            sum+=b;
        if(c<0)
            sum+=c;
        if(d<0)
            sum+=d;
        
        System.out.println(sum);
    }
}
